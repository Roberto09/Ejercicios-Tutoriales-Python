"""Clases y Objetos (programacion orientada a objetos)"""

#Clase: la clase es una plantilla de un objetos
class Lapiz:
	color="Amarillo"
	contiene_borrador=True
	Altura=2

#Objeto: Es una entidad con porpiedades que puede realizar o no acciones
lapiz_generico=Lapiz()
print(lapiz_generico.color)
print(lapiz_generico.contiene_borrador)
print(lapiz_generico.Altura)